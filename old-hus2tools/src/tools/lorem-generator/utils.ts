function randomItem<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

function randomInt(min: number, max: number): number {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

export function generateChineseParagraph(sentenceCount: number): string {
  const sentences: string[] = [];
  for (let i = 0; i < sentenceCount; i++) {
    const wordCount = randomInt(8, 20);
    const words: string[] = [];
    for (let j = 0; j < wordCount; j++) {
      words.push(randomItem(CHINESE_WORDS));
      if (j < wordCount - 1 && Math.random() < 0.3) {
        words.push(randomItem(CHINESE_CONNECTORS));
      }
    }
    sentences.push(words.join('') + randomItem(CHINESE_SENTENCE_ENDS));
  }
  return sentences.join('');
}

export function generateEnglishParagraph(sentenceCount: number): string {
  const sentences: string[] = [];
  for (let i = 0; i < sentenceCount; i++) {
    const wordCount = randomInt(8, 20);
    const words: string[] = [];
    for (let j = 0; j < wordCount; j++) {
      words.push(randomItem(ENGLISH_WORDS));
    }
    const sentence = words.join(' ') + '.';
    sentences.push(sentence.charAt(0).toUpperCase() + sentence.slice(1));
  }
  return sentences.join(' ');
}

export function generateChineseTitle(): string {
  const len = randomInt(4, 8);
  const chars: string[] = [];
  for (let i = 0; i < len; i++) {
    chars.push(randomItem(CHINESE_WORDS));
  }
  return chars.join('').slice(0, len);
}

export function generatePhoneNumber(): string {
  return randomItem(PHONE_PREFIXES) + String(Math.floor(Math.random() * 100000000)).padStart(8, '0');
}

export function generateEmail(): string {
  const name = 'user' + Math.floor(Math.random() * 100000);
  return name + '@' + randomItem(EMAIL_DOMAINS);
}

export function generateAddress(): string {
  const province = randomItem(PROVINCES);
  const district = randomItem(DISTRICTS);
  const road = randomItem(ROADS);
  const number = randomInt(1, 999);
  const building = randomInt(1, 30);
  const unit = randomInt(1, 6);
  const room = randomInt(101, 1899);
  return `${province}${district}${road}${number}号${building}栋${unit}单元${room}室`;
}

export function generateCompany(): string {
  return randomItem(COMPANY_PREFIXES) + randomItem(COMPANY_MIDDLES) + randomItem(COMPANY_SUFFIXES);
}

export type LoremType = 'chinese-paragraph' | 'english-paragraph' | 'chinese-title' | 'phone' | 'email' | 'address' | 'company';

export function generateLorem(type: LoremType, count: number): string[] {
  return Array.from({ length: count }, () => {
    switch (type) {
      case 'chinese-paragraph': return generateChineseParagraph(randomInt(3, 6));
      case 'english-paragraph': return generateEnglishParagraph(randomInt(3, 6));
      case 'chinese-title': return generateChineseTitle();
      case 'phone': return generatePhoneNumber();
      case 'email': return generateEmail();
      case 'address': return generateAddress();
      case 'company': return generateCompany();
    }
  });
}
